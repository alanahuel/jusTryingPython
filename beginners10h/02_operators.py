#!/bin/python3

new_line = "\n"

# Aritmetics Operators

print(3 + 4) # 7
print(3 - 4) # -1
print(3 * 4) # 12
print(3 / 4) # 0.75
print(10 % 3) # 1 
print(10 // 3) # 3
print(2 ** 3) # 8
print(2 ^ 3) # 1
print(new_line)

print("Hi " + "Python!" + " What's up?")
print("Hi " + str(5))
print("HI" * 5)
print(new_line)

# Comparative Operators

print(3 > 4) # false
print(4 < 3) # false
print(4 <= 3) # false
print(3 >= 4) # false
print(3 == 4) # false
print(3!= 4) # true
print(new_line)

# Logic Operators

print(3 > 4 and "Hi" > "Python")
print(3 > 4 and "Hi" < "Python")
print(3 > 4 or "Hi" < "Python")
print(3 > 4 or "Hi" > "Python")
print(not (3 > 4))
